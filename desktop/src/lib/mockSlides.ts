import type { PresentationSlide } from '../types/desktop'

type DeckOptions = {
  deckTitle: string
  sourceLabel: string
  count: number
}

const palettes = [
  ['#0e1b29', '#13334d', '#f7934f', '#d9f55f'],
  ['#170f2a', '#2b1747', '#54d2ff', '#ffe77a'],
  ['#101d1c', '#0f3a35', '#8cff98', '#d6ff78'],
  ['#211514', '#462422', '#ff9c66', '#ffd8a8'],
  ['#101828', '#19304a', '#8fdcff', '#9fe870'],
  ['#1c1122', '#3c1f4f', '#f08cff', '#7af7db'],
] as const

const sections = [
  'Introduction',
  'Workflow',
  'Performance',
  'System Map',
  'Live Status',
  'Next Step',
]

function escapeXml(value: string): string {
  return value
    .replaceAll('&', '&amp;')
    .replaceAll('<', '&lt;')
    .replaceAll('>', '&gt;')
    .replaceAll('"', '&quot;')
    .replaceAll("'", '&apos;')
}

