function toDataUrl(svg: string): string {
  return `data:image/svg+xml;charset=UTF-8,${encodeURIComponent(svg)}`
}

function createDeck({ deckTitle, sourceLabel, count }: DeckOptions): PresentationSlide[] {
  return Array.from({ length: count }, (_, index) => ({
    id: `${deckTitle.toLowerCase().replaceAll(/\s+/g, '-')}-${index + 1}`,
    title: `${deckTitle} / ${sections[index % sections.length]}`,
    imageUrl: toDataUrl(
      slideSvg({
        deckTitle,
        sourceLabel,
        slideIndex: index,
        total: count,
      }),
    ),
  }))
}

export function createDemoDeck(): PresentationSlide[] {
  return createDeck({
    deckTitle: 'Demo Deck',
    sourceLabel: 'demo',
    count: 5,
  })
}

export function createLocalDeck(fileName: string): PresentationSlide[] {
  const deckTitle = fileName.replace(/\.[^.]+$/, '') || 'Local Presentation'
  return createDeck({
    deckTitle,
    sourceLabel: 'file',
    count: 6,
  })
}

export function createRemoteDeck(): PresentationSlide[] {
  return createDeck({
    deckTitle: 'Mobile Sync Deck',
    sourceLabel: 'remote',
    count: 4,
  })
}
